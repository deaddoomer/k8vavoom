<h2>About</h2><div>
Vavoom is a source port based on sources of Doom, Heretic, Hexen and a little
bit from Quake. Supported platforms are DOS, Windows and Linux.
</div>

<h2>Requirements</h2><div>
To play Vavoom you need main wad file from one of the following games:
<ul>
<li>Doom shareware</li>
<li>Doom registered</li>
<li>Ultimate Doom</li>
<li>Doom II: Hell on Earth</li>
<li>Doom II: TNT Evilution</li>
<li>Doom II: The Plutonia experiment</li>
<li>Heretic shareware</li>
<li>Heretic registered</li>
<li>Hexen</li>
<li>Strife</li>
</ul>
If you have multiple IWADs, you can use command line params -doom, -doom2,
-heretic or -hexen.
</div>

<h2>Differences from original game</h2><div>
<ul>
<li>Polygonal engine with colored lighting, with software mode, OpenGL
and Direct3D support, and any resolutions up to 1600x1200</li>
<li>Translucency. Some things are translucent, also spectres' partial
invisibility is replaced with 10% translucency, so they are really hard
to see now</li>
<li>Complete support for freelook (look up & down) in all games</li>
<li>A powerful language to describe game logic, Vavoom C - now you can
forget about DeHackEd and DDF!</li>
<li>100% client/server architecture with in-game joining</li>
<li>Quake-style console, with key bindings</li>
<li>Ability to play AudioCD tracks</li>
<li>Indirect support for DeHackEd - utility to generate Vavoom progs out
of DEH files</li>
<li>Crosshair</li>
<li>Jumping</li>
<li>And many other cool things...</li>
</ul>

